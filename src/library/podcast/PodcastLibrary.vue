<template>
  <div>
    <div class="d-flex justify-content-between align-items-center mb-3">
      <ul class="nav-underlined">
        <li>
          <router-link :to="{... $route, params: {... $route.params, sort: null }}">
            Recently updated
          </router-link>
        </li>
        <li>
          <router-link :to="{... $route, params: {... $route.params, sort: 'a-z' }}">
            A-Z
          </router-link>
        </li>
      </ul>
      <OverflowMenu>
        <ContextMenuItem v-if="$auth.hasRole('podcast')" icon="plus" @click="showPodcastAdd = true">
          Add
        </ContextMenuItem>
        <ContextMenuItem icon="refresh" @click="refresh()">
          Refresh
        </ContextMenuItem>
      </OverflowMenu>

      <b-modal
        v-model="showPodcastAdd"
        title="Add podcast"
        size="md"
        @ok="addPodcast"
      >
        <template #modal-header-close>
          <Icon icon="x" />
        </template>
        <form class="form-inline my-2 my-lg-0" @submit.prevent="addPodcast">
          <input
            v-model="addedPodcastUrl"
            class="form-control" type="url"
            style="width: 100%"
            placeholder="Podcast URL">
        </form>
      </b-modal>
    </div>
    <ContentLoader v-slot :loading="loading">
      <Tiles square>
        <Tile v-for="item in sortedItems" :key="item.id"
              :image="item.image"
              :to="{name: 'podcast', params: { id: item.id } }"
              :title="item.name">
          <template #text>
            <strong>{{ item.trackCount }}</strong> episodes
          </template>
        </Tile>
      </Tiles>
    </ContentLoader>
    <p v-if="sortedItems.length === 0">
      No podcasts have been added to the library yet.
    </p>
    <p v-if="sortedItems.length === 0 && $auth.hasRole('podcast')">
      <a href="#" @click.prevent="showPodcastAdd = true">
        Click here to add a podcast now.
      </a>
    </p>
  </div>
</template>
<script lang="ts">
  import { defineComponent } from '@vue/composition-api'
  import { orderBy } from 'lodash-es'

  export default defineComponent({
    props: {
      sort: { type: String, default: null },
    },
    data() {
      return {
        items: null as null | any[],
        showPodcastAdd: false,
        addedPodcastUrl: '',
      }
    },
    computed: {
      loading(): boolean {
        return this.items === null
      },
      sortedItems(): any[] {
        return this.sort === 'a-z'
          ? orderBy(this.items, 'name')
          : orderBy(this.items, 'albumCount', 'desc')
      },
    },
    async created() {
      this.items = await this.$api.getPodcasts()
    },
    methods: {
      async refresh() {
        await this.$api.refreshPodcasts()
        this.items = await this.$api.getPodcasts()
      },
      addPodcast() {
        this.showPodcastAdd = false
        if (this.addedPodcastUrl) {
          this.$api.addPodcast(this.addedPodcastUrl).then(() => {
            setTimeout(this.refresh, 1500)
          })
        }
      },
    }
  })
</script>
